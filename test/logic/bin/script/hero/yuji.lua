[Q]于吉蛊惑【杀】不声明属性被人质疑，验明时真假如何判断？[A]只要翻开后为【杀】，则算作真，且【杀】的属性按原牌结算；否则算作假。
[Q]于吉蛊惑【杀】声明属性（无属性/火属性/雷属性）被人质疑，验明时真假如何判断？[A]如果原牌不是【杀】或虽然是【杀】但与声明的属性不同，则算作假；如果原牌是【杀】且属性与声明的相同，则算作真。
[Q]于吉蛊惑【杀】不声明属性无人质疑，此【杀】的属性如何处理？[A]无人质疑时，原牌仍然要翻开。若原牌是【杀】，按原牌属性结算；若原牌不是【杀】，按无属性【杀】结算。
[Q]于吉蛊惑【杀】声明属性（无属性/火属性/雷属性）无人质疑，此【杀】的属性如何处理？[A]无论翻开后的原牌是什么属性，此【杀】均按声明的属性结算。
[Q]于吉蛊惑【杀】时无人质疑，此【杀】的花色如何处理？[A]以翻开后的原牌花色为参照。
[Q]于吉蛊惑无属性【杀】成功，能否发动已装备的【朱雀羽扇】特效？[A]可以，在验明后发动。


