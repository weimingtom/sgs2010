【薄幸的美人·甄姬】
WEI007　KayaK
武将技：
倾国——你可以将你的黑色手牌当【闪】使用（或打出）。
洛神——回合开始阶段，你可以进行判定：若为黑色，立即获得此生效后的判定牌，并可以再次使用洛神——如此反复，直到出现红色或你不愿意判定了为止。

[Q]甄姬面前有需要判定的延时锦囊时，甄姬发动【洛神】，并获得1张【无懈可击】，在【洛神】发动结束后，甄姬是否可以使用此张【无懈可击】来抵消延时锦囊？[A]可以。
[Q]甄姬是否可以先判定延时锦囊，然后再发动【洛神】？[A]不可以，【洛神】只有在回合开始阶段可以发动，如果甄姬直接进入判定阶段，就视为放弃发动【洛神】。

