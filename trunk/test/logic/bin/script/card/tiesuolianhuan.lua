1、先铁索连环AB，再铁索连环CD，这时候ABCD是否在同个连环，还是在两个连环？
ABCD在同个连环，铁索连环是一个状态，而不是一条一条绳子，所以只要被铁索连环影响而横置身份牌的玩家全都处于同一个铁索连环的状态。
2、铁索连环过程中，比如ABCD连环，如果A受到1点火属性伤害，从而触发连环，B有藤甲，CD没有，这时BCD各受到多少伤害？
在没有其他条件的前提下，这时B受到1+1=2点火属性伤害，CD仍然受到1点火属性伤害，也就是说，铁索连环中的玩家受到的伤害=第一个受伤害并触发铁索连环的玩家所收到的伤害+自己装备可能发生的加成或者限制，跟自己的上家（非触发者）没有关系。
3、如果ABCD铁索连环，B是小乔，A受到1点火属性伤害，B天香给C，这时候各人的受伤害情况和武将牌状态各在什么时刻发生变化，最后是什么状态？这是A受到1点火属性伤害，武将牌重置；B天香给C，C受到1点火属性伤害，同时马上重置武将牌，B因为自己没有收到伤害，所以武将牌不重置，仍然处于铁索连环状态；C不会再受到A传过来的铁索连环伤害，因为被天香后已经重置；D仍然受到A引发的铁索连环伤害，受到1点火属性伤害。
4、可否对1个玩家铁索连环？
可以，比如先对A出铁索连环，然后再对BC出一张铁索连环，则ABC处于同个连环状态。
