【铁索连环】
定义：连环，出牌阶段使用，选择1至2个角色，分别横置或重置这些角色。处于“连环状态”。重铸，出牌阶段，你可以从手里弃掉这张牌，然后摸1张牌。

[Q]黄月英发动【铁索连环】的重铸效果时，能否发动【集智】？[A]不能。当你重铸【铁索连环】时，你并没有使用它。
[Q]使用【铁索连环】时，能否用无懈可击抵消？[A]重铸【铁索连环】不能被无懈可击，当发动连环效果时，无懈可击抵消其对某一角色的效果。
[Q]处于连环状态的角色，受到伤害时如何结算？[A]当连环状态的角色受到普通伤害时（如普杀、决斗、南蛮入侵、万箭齐发等），正常结算，且不解除连环状态。而当其受到属性伤害时（火焰伤害：使用火杀命中，神周瑜的【业炎】技能或者火攻成功造成的伤害或雷电伤害：使用雷杀命中，张角的【雷击】技能或者闪电造成的伤害），则从其开始触发连环。该伤害按照行动顺序，同属性、同程度地依次传导到每一个连环状态的角色，同时这些角色重置，解除连环状态。
[Q]当A与B处于连环状态时，对C与D使用【铁索连环】，那么此4人是否处于同一连环状态？[A]是，当其中1个角色受到属性伤害时，4人一同受到该伤害。
[Q]连环状态中的角色受到伤害发动相应技能时如何结算？[A]刚烈、反馈等技能的对象均为伤害来源而非触发连环的角色。当这些技能结算完毕后再结算下一个连环状态的角色受到的属性伤害。
[Q]场上只有1个角色处于连环状态时，受到属性伤害是否需要重置？[A]需要。
[Q]处于连环状态的小乔受到属性伤害并发动【天香】，是否需要重置？[A]不需要。小乔发动天香时不会触发连环。
[Q]处于连环状态的小乔受到属性伤害并天香给同在连环状态下但尚未结算连环伤害的角色，如何处理？[A]该角色受到属性伤害，解除连环状态，不再触发连环并结算天香摸牌。随后之前的连环伤害继续结算。
[Q]连环状态的角色受到传导的伤害死亡，由谁承担奖惩？[A]最初的伤害源头。如果在结算奖惩时，伤害源头已经死亡，则无人承担奖惩。只要满足游戏结束条件出现，则中止结算，游戏结束。

