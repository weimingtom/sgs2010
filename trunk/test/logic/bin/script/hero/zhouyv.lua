【大都督・周瑜】
WU005　KayaK
武将技：
英姿――摸牌阶段，你可以额外摸一张牌。
反间――出牌阶段，你可以令一名其他角色说出一种花色，然后获得你的一张手牌并展示之，若此牌不为其所述之花色，你对该角色造成1点伤害。每阶段限一次。

[Q]小乔抽取周瑜的黑桃，是否算红桃？[A]是，因为此牌为小乔手牌，受【红颜】影响。
[Q]周瑜发动【反间】时，如果有多张手牌，牌的放置顺序由谁决定？[A]由周瑜决定。
[Q]周瑜对小乔发动【反间】，小乔天香给曹操，谁获得周瑜的反间牌？[A]小乔。
[Q]周瑜受到【兵粮寸断】的效果时，能否发动英姿？[A]不能。
[Q]周瑜发动【反间】技能造成的伤害是什么属性？[A]普通伤害。

