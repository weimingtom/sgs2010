【藤甲】
防具效果：锁定技，【南蛮入侵】、【万箭齐发】和普通【杀】对你无效。每次受到火焰伤害时，该伤害+1。

[Q]装备了【藤甲】结算决斗时，对方所打出的普杀是否有效？[A]有效。
[Q]装备【青G剑】使用火杀攻击装备【藤甲】的角色成功时，该角色受到多少伤害？[A]青G剑无视防具，不发动+1伤害的效果。
[Q]装备【青G剑】对装备【藤甲】的角色火攻成功时，是否发动藤甲的+1火焰伤害效果？[A]发动。青G剑无视防具的效果对杀以外的结算无效。
[Q]2个装备了【藤甲】的连环状态的角色，其中之一受到1点火焰时，另一方受到几点伤害？[A]3点。
[Q]张角装备了【藤甲】在结算万箭齐发或者普杀时能否主动出【闪】？[A]不能。锁定技强制发动。藤甲的普杀无效与仁王盾黑杀无效处理原则相同。类似地，小乔不能主动弃牌发动天香，曹操不能发动奸雄等。

