【前将军・张辽】
WEI004　Kayak
武将技：
突袭――摸牌阶段，你可以放弃摸牌，然后从至多两名（至少一名）角色的手牌里各抽取一张牌。
★摸牌阶段，你一旦发动突袭，就不能从牌堆获得牌；只剩一名其他角色时，你就只能选择着一名角色；若此时其他任何人都没有手牌，你就不能发动突袭。

[Q]当场上有三名或者更多的角色时，张辽发动【突袭】是否可以只从一名角色的手牌选择获得一张牌？[A]可以。
[Q]张辽发动【突袭】时，是否可以1张牌也不获得？[A]不可以。张辽发动【突袭】至少要存在一名有手牌的角色。如果不能满足此条件，张辽不能发动【突袭】。
[Q]张辽发动【突袭】时，是否可以从一名角色的手牌里获得2张牌？[A]不可以。
[Q]张辽发动【突袭】时，是否可以从一名角色的手牌里获得1张牌，再从牌堆摸1张牌？[A]不可以，发动【突袭】就不能从牌堆获得牌。
[Q]张辽发动【突袭】时，是否可以摸1张牌后先看是什么牌，再摸第2张牌？[A]不可以，张辽必须先摸完牌才能看。
[Q]张辽受到【兵粮寸断】的效果时，能否发动【突袭】？[A]不可以。

